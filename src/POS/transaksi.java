    private void txtjumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtjumlahActionPerformed
       int harga = Integer.parseInt(txtharga.getText());
       int jumlah = Integer.parseInt(txtjumlah.getText());
       int total = harga * jumlah;
       
       txttotal.setText(String.valueOf(total));
       txtdiskon.requestFocus();
    }//GEN-LAST:event_txtjumlahActionPerformed

    private void txtdiskonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtdiskonActionPerformed
        int diskon = Integer.parseInt(txtdiskon.getText());
        int total = Integer.parseInt(txttotal.getText());
        double hasil = (1-diskon/100.0)*total;
        txttotalbayar.setText(String.valueOf(hasil));
        txtbayar.requestFocus();
         
        
    }//GEN-LAST:event_txtdiskonActionPerformed

    private void txtbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtbayarActionPerformed
        int bayar = Integer.parseInt(txtbayar.getText());
        double totalbayar = Double.parseDouble(txttotalbayar.getText());
        double hasil = bayar - totalbayar;
        if(bayar  < totalbayar){
            JOptionPane.showMessageDialog(this, "Uang yang Anda bayarkan kurang");
        }else{
            txtkembali.setText(String.valueOf(hasil));
            btnsimpan.setEnabled(true);
        }
        
    }//GEN-LAST:event_txtbayarActionPerformed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        try {
             String sql = "INSERT INTO pos(namabarang,hargabarang,jumlahbarang,diskon) VALUES('"+txtnama.getText()+"','"+txtharga.getText()+"','"+txtjumlah.getText()+"','"+txtdiskon.getText()+"')";
        database.eksekusi(sql);
        JOptionPane.showMessageDialog(rootPane, "Data Berhasil Disimpan");
        tampil();
        txtnama.setText("");
        txtharga.setText("");
        txtjumlah.setText("");
        txtdiskon.setText("");
        txttotal.setText("");
        txttotalbayar.setText("");
        txtbayar.setText("");
        txtkembali.setText("");
        } catch (Exception e) {
            System.out.println("Error simpan"+e.getMessage());
        }
       
    }//GEN-LAST:event_btnsimpanActionPerformed

