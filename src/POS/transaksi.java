    private void tampil(){
        DefaultTableModel model = (DefaultTableModel) tblpos.getModel();
        model.setRowCount(0);
        try {
            ResultSet row = database.ambildata("SELECT * FROM view_pos");
            while(row.next()){
                Object data[] = new Object[]{
                    row.getString(1),
                    row.getString(2),
                    row.getString(3),
                    row.getString(4),
                    row.getString(5),
                    row.getString(6),
                    row.getString(7)
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("error tampil" + e.getMessage()); 
        }
    }

