    private void btnlaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnlaporanActionPerformed
            // TODO add your handling code here:
        try {
              File  file = new File("E:\\laporan.txt");
                if(!file.exists()){
                file.createNewFile();
            }
                FileWriter fw = new FileWriter(file.getAbsoluteFile());
                BufferedWriter bw = new BufferedWriter(fw);
                for(int i = 0; i < tblpos.getRowCount(); i++){
                    for(int j = 0; j < tblpos.getColumnCount(); j++){
                        bw.write(tblpos.getModel().getValueAt(i, j)+" | ");
                    }
                    bw.write("\n______________________________________________________________\n");
                }
                bw.close();
                fw.close();
                JOptionPane.showMessageDialog(rootPane, "Laporan Sudah disimpan ke Drive E");
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_btnlaporanActionPerformed

